function summarizeManagedRoots(pack: AgentPack): string {
  const roots = new Set<string>();
  for (const targetFile of pack.targetFiles) {
    roots.add(collapseTargetRoot(pack.packId, targetFile.path));
  }
  return [...roots].sort().map((entry) => `\`${entry}\``).join(', ');
}

function collapseTargetRoot(packId: string, targetPath: string): string {
  const parts = targetPath.split('/');
  if (packId === 'cursor') return parts.slice(0, 3).join('/');
  if (packId === 'copilot') return parts.length > 2 ? parts.slice(0, 2).join('/') : parts[0];
  return parts.slice(0, 2).join('/');
}

function summarizeFormats(pack: AgentPack): string {
  const formats = new Set(pack.targetFiles.map((file) => formatForPath(file.path)));
  return [...formats].sort().join(', ');
}

function formatForPath(targetPath: string): string {
  if (targetPath.endsWith('.toml')) return 'TOML';
  if (targetPath.endsWith('.md')) return 'Markdown';
  if (targetPath.endsWith('.prompt.md')) return 'Prompt Markdown';
  if (targetPath.endsWith('.agent.md')) return 'Agent Markdown';
  if (targetPath.endsWith('.yml') || targetPath.endsWith('.yaml')) return 'YAML';
  return path.extname(targetPath).replace(/^\./, '').toUpperCase() || 'text';
}

function hashJson(value: unknown): string {
  return createHash('sha256').update(JSON.stringify(value), 'utf8').digest('hex');
}

function writeMatrix(): void {
  const outputPath = path.join(root, agentMatrixOutputPath);
  mkdirSync(path.dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, renderAgentMatrixMarkdown(), 'utf8');
}

function checkMatrix(): void {
  const outputPath = path.join(root, agentMatrixOutputPath);
  const expected = renderAgentMatrixMarkdown();
  const actual = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : '';
  if (actual !== expected) {
    console.error(`[agent-matrix] ${agentMatrixOutputPath} is stale. Run: node --strip-types scripts/render-agent-matrix.ts`);
    process.exitCode = 1;
    return;
  }
  console.log(`[agent-matrix] ok (${collectAgentPackRows().length} packs)`);
}

const isDirectRun = process.argv[1]
  ? path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)
  : false;

if (isDirectRun) {
  if (process.argv.includes('--check')) {
    checkMatrix();
  } else if (process.argv.includes('--stdout')) {
    process.stdout.write(renderAgentMatrixMarkdown());
  } else {
    writeMatrix();
    console.log(`[agent-matrix] wrote ${agentMatrixOutputPath}`);
  }
}
