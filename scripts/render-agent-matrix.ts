interface AgentPackMatrixRow {
  readonly agent: string;
  readonly packId: string;
  readonly agentTarget: string;
  readonly managedRoots: string;
  readonly fileFormats: string;
  readonly managedFiles: number;
  readonly firstCommand: string;
  readonly sourceHash: string;
}

export function collectAgentPackRows(): AgentPackMatrixRow[] {
  return builtInAgentPacks.map((pack) => ({
    agent: displayNameByPackId[pack.packId] ?? pack.name.replace(/ Agent Pack$/, ''),
    packId: pack.packId,
    agentTarget: pack.agentTarget,
    managedRoots: summarizeManagedRoots(pack),
    fileFormats: summarizeFormats(pack),
    managedFiles: pack.targetFiles.length,
    firstCommand: '`node atm.mjs next --json`',
    sourceHash: `\`${(pack.sourceHash ?? hashJson(pack.targetFiles)).slice(0, 16)}\``
  }));
}

