export function runPreCommitHook(cwd: string) {
  const root = path.resolve(cwd);
  const committingTaskIdForHook = typeof process.env.ATM_COMMIT_TASK_ID === 'string' ? process.env.ATM_COMMIT_TASK_ID.trim() : null;
  const crossTaskBlock = detectCrossTaskMutation(root, committingTaskIdForHook, 'pre-commit');
  if (crossTaskBlock) {
    recordIncidentFlag(root, crossTaskBlock);
    throw new CliError('ATM_CROSS_TASK_MUTATION_BLOCKED', `Cross-task mutation incident detected: files owned by active task ${crossTaskBlock.conflictTaskId} are mutated. File(s): ${crossTaskBlock.conflictFiles.join(', ')}. Recovery: ${crossTaskBlock.recoveryLane}`, {
      exitCode: 1,
      details: {
        taskId: committingTaskIdForHook,
        conflictTaskId: crossTaskBlock.conflictTaskId,
        conflictFiles: crossTaskBlock.conflictFiles,
        recoveryLane: crossTaskBlock.recoveryLane
      }
    });
  }
  const scopedIndexActive = typeof process.env.GIT_INDEX_FILE === 'string' && process.env.GIT_INDEX_FILE.trim().length > 0;
  const residueCandidates = uniqueSorted([
    ...runGitLines(root, ['diff', '--name-only']),
    ...runGitLines(root, ['ls-files', '--others', '--exclude-standard'])
  ].map(normalizeRelativePath).filter(Boolean));
  const residueReport = inspectAutoGeneratedResidue(resolveResidueTaskId(root), residueCandidates);
  const autoCleanedResidue = cleanupAutoGeneratedResidue(root, residueReport.autoCleanSafe);
  const stagedFiles = readStagedFiles(root).filter((entry) => entry !== gitHeadEvidencePaths.legacyJson && entry !== gitHeadEvidencePaths.jsonl);
  const gitIndexDiagnostic = inspectGitIndexAccess(root);
  const encodingReport = scanEncoding(root, stagedFiles);
  const frameworkStatus = createFrameworkModeStatus({ cwd: root, files: stagedFiles });
  const allowAdopterInfrastructureSync = isAdopterInfrastructureSyncCommit(
    stagedFiles.length > 0 ? stagedFiles : frameworkStatus.changedFiles
  );
  const activeDirectionLocks = readActiveTaskDirectionLocks(root);
  const activeQuickfixLock = readActiveQuickfixLock(root);
  const taskGovernedCommitAllowedFiles = collectTaskGovernedCommitAllowedFiles(root, committingTaskIdForHook);
  // TASK-AAO-0136: collect close-commit-window allowedFiles (short-lived locks
  // opened by tasks close / tasks reconcile). These authorize landing the close
  // artifacts after the regular direction lock has already released.
  const activeCloseCommitWindows = readActiveCloseCommitWindows(root);
  const closeCommitWindowAllowedFiles = uniqueSorted(activeCloseCommitWindows.flatMap((win) => [
    ...win.allowedFiles,
    `.atm/history/tasks/${win.taskId}.json`,
    `.atm/history/evidence/${win.taskId}.json`,
    `.atm/history/evidence/${win.taskId}.bundle-manifest.json`,
    `.atm/history/evidence/${win.taskId}.closure-packet.json`,
    `.atm/history/task-events/${win.taskId}/**`
  ]));
  const closeCommitWindowPlanningMirrorFiles = collectCloseCommitWindowPlanningMirrorFiles(root);
  const relevantDirectionLocks = selectRelevantDirectionLocksForCommit({
    activeDirectionLocks,
    stagedFiles,
    committingTaskId: committingTaskIdForHook,
    taskGovernedCommitAllowedFiles,
    closeCommitWindowAllowedFiles,
    closeCommitWindowPlanningMirrorFiles
  });
  const directionLockAllowedFiles = uniqueSorted(relevantDirectionLocks.flatMap((lock) => lock.allowedFiles));
  const directionLockAllowedFilesDiagnoses: readonly TaskDirectionAllowedFilesDiagnosis[] = activeDirectionLocks
    .map((lock) => diagnoseTaskDirectionLockAllowedFiles(root, lock.taskId));
  const directionLockAllowedFilesMismatches = directionLockAllowedFilesDiagnoses.filter((entry) => entry.mismatches.length > 0);
  const checkpointClosedTaskAllowedFiles = collectStagedBatchCheckpointScopeFiles(root, stagedFiles);
  const checkpointCoversFrameworkCriticalFiles = frameworkStatus.criticalChangedFiles.length > 0
    && frameworkStatus.criticalChangedFiles.every((entry) => isPathAllowedByTaskDirection(entry, checkpointClosedTaskAllowedFiles));
  const directionLockCoversFrameworkCriticalFiles = frameworkStatus.criticalChangedFiles.length > 0
    && frameworkStatus.criticalChangedFiles.every((entry) => isPathAllowedByTaskDirection(entry, directionLockAllowedFiles));
  const blockingFrameworkIssues = frameworkStatus.blockers.filter((entry) => {
    if (entry === 'git-head-evidence-missing') return false;
    if (entry === 'active-framework-claim-required' && (checkpointCoversFrameworkCriticalFiles || directionLockCoversFrameworkCriticalFiles)) return false;
    if (entry === 'closure-authority-belongs-to-target-repo' && allowAdopterInfrastructureSync) return false;
    return true;
  });
  const frameworkTempClaimAllowedFiles = relevantDirectionLocks.length > 0
    ? collectFrameworkTempClaimAllowedFiles(root)
    : [];
  const directionLockPlanningMirrorPaths = uniqueSorted(relevantDirectionLocks.flatMap((lock) => lock.planningMirrorPaths ?? []));
  const directionLockAllowsPlanningMirror = relevantDirectionLocks.some((lock) => lock.allowPlanningMirror === true);
  const taskOpenImportBundle = inspectTaskOpenImportBundleStagedArtifacts(root, stagedFiles);
  const directionLockDriftFiles = relevantDirectionLocks.length > 0 && !allowAdopterInfrastructureSync
    ? taskOpenImportBundle.ok
      ? []
      : stagedFiles
      .filter((entry) => !isTaskDirectionPreCommitExempt(entry))
      .filter((entry) => !isPathAllowedByTaskDirection(entry, directionLockAllowedFiles))
      .filter((entry) => !isPathAllowedByTaskDirection(entry, checkpointClosedTaskAllowedFiles))
      .filter((entry) => !isPathAllowedByTaskDirection(entry, frameworkTempClaimAllowedFiles))
      .filter((entry) => !isPathAllowedByTaskDirection(entry, taskGovernedCommitAllowedFiles))
      .filter((entry) => !isPathAllowedByTaskDirection(entry, closeCommitWindowAllowedFiles))
    : [];
  const planningMirrorDriftFiles = relevantDirectionLocks.length > 0 && !allowAdopterInfrastructureSync && directionLockPlanningMirrorPaths.length > 0 && !directionLockAllowsPlanningMirror
    ? stagedFiles
      .filter((entry) => !isTaskDirectionPreCommitExempt(entry))
      .filter((entry) => isPlanningMirrorPath(entry, directionLockPlanningMirrorPaths))
      .filter((entry) => !isPathAllowedByTaskDirection(entry, taskGovernedCommitAllowedFiles))
      .filter((entry) => !isPathAllowedByTaskDirection(entry, closeCommitWindowPlanningMirrorFiles))
    : [];
  const quickfixDriftFiles = activeQuickfixLock && !allowAdopterInfrastructureSync
    ? stagedFiles
      .filter((entry) => !isTaskDirectionPreCommitExempt(entry))
      .filter((entry) => !isPathAllowedByScope(entry, activeQuickfixLock.allowedFiles))
    : [];
  const quickfixFileLimitExceeded = activeQuickfixLock
    ? stagedFiles.filter((entry) => !entry.startsWith('.atm/')).length > activeQuickfixLock.maxFiles
    : false;
  const quickfixChangedLineCount = activeQuickfixLock
    ? readStagedChangedLineCount(root, stagedFiles.filter((entry) => !entry.startsWith('.atm/')))
    : 0;
  const quickfixLineLimitExceeded = activeQuickfixLock
    ? quickfixChangedLineCount > activeQuickfixLock.maxChangedLines
    : false;
  const protectedStateReport = inspectProtectedAtmStateChanges(root, stagedFiles);
  const emergencyUseAuditReport = inspectEmergencyUseAudit(root, stagedFiles);
  const taskCardStatusReport = inspectTaskCardStatusChanges(root, stagedFiles);
  const commitAttributionReport = inspectCommitAttribution(root, stagedFiles);
  const explicitActorWarning = stagedFiles.length > 0 && !normalizeOptionalText(process.env.ATM_COMMIT_ACTOR_ID)
    ? message('warn', 'ATM_HOOK_COMMIT_ACTOR_NOT_EXPLICIT', 'No explicit commit actor was provided. Configure this editor/agent with its own actor identity instead of relying on repo default identity.', {
      clearStaleDefaultCommand: 'node atm.mjs identity clear --json',
      requiredCommand: 'node atm.mjs identity clear --json && node atm.mjs identity set --actor <actor-id> --editor <editor-id> --git-name "<git user.name>" --git-email "<git user.email>" --json',
      env: 'ATM_COMMIT_ACTOR_ID or ATM_ACTOR_ID'
    })
    : null;
  // TASK-CID-0024: same-file parallel claims are first-class. Multiple active
  // claims covering one staged file must not block by themselves; only
  // ambiguous staged ownership without steward/broker evidence blocks.
  const sameFileClaimReport = allowAdopterInfrastructureSync
    ? { ok: true, committingTaskId: null, committingClaimIntent: null, multiClaimFiles: [], stewardCoveredFiles: [], findings: [] } satisfies SameFileClaimOwnershipReport
    : inspectSameFileClaimOwnership({
      cwd: root,
      stagedFiles,
      activeDirectionLocks: relevantDirectionLocks,
      exemptAllowedFileSets: [
        checkpointClosedTaskAllowedFiles,
        taskGovernedCommitAllowedFiles,
        closeCommitWindowAllowedFiles,
        frameworkTempClaimAllowedFiles
      ]
    });
  const taskAudit = auditTasks(root);
  const commandRuns = frameworkStatus.criticalChangedFiles.length > 0
    ? runRequiredFrameworkValidators(root, frameworkStatus.requiredGates)
    : [];
  const validatorRunTriage = triageForeignTaskflowValidatorRuns({
    cwd: root,
    stagedFiles,
    activeDirectionLocks,
    failedRuns: commandRuns.filter((entry) => entry.exitCode !== 0)
  });
  const failedValidatorRuns = validatorRunTriage.blockingRuns;
  const staleLocks = frameworkStatus.staleLocks;
  const releasableStaleLock = staleLocks.find(isFrameworkStaleLockReleasable) ?? null;
  const baseClaimCommand = buildFrameworkTempClaimCommand(frameworkStatus.criticalChangedFiles, 'temporary framework maintenance before commit', releasableStaleLock?.actorId ?? null);
  const frameworkClaimCommand = blockingFrameworkIssues.includes('active-framework-claim-required')
    ? baseClaimCommand
    : blockingFrameworkIssues.includes('framework-stale-lock-cleanup-required') && releasableStaleLock
      ? buildFrameworkStaleCleanupCommand(releasableStaleLock, frameworkStatus.criticalChangedFiles, 'temporary framework maintenance before commit')
    : null;
  const crossFileConsistencyFindings = checkStageTimeCrossFileConsistency(root, stagedFiles);
  // TASK-AAO-0136: only staged or invariant audit findings block the commit;
  // tree-wide pre-existing audit findings get demoted to advisory warnings.
  const stagedFilesSet = new Set(stagedFiles);
  const blockingTaskAuditFindings = taskAudit.findings.filter((entry) => {
    if (entry.level !== 'error') return false;
    if (INVARIANT_TASK_AUDIT_CODES.has(entry.code)) return true;
    return 'path' in entry && typeof entry.path === 'string' && stagedFilesSet.has(entry.path);
  });
  const baseOk = encodingReport.ok
    && gitIndexDiagnostic.ok
    && blockingFrameworkIssues.length === 0
    && planningMirrorDriftFiles.length === 0
    && directionLockDriftFiles.length === 0
    && quickfixDriftFiles.length === 0
    && !quickfixFileLimitExceeded
    && !quickfixLineLimitExceeded
    && commitAttributionReport.ok
    && protectedStateReport.ok
    && emergencyUseAuditReport.ok
    && taskCardStatusReport.ok
    && sameFileClaimReport.ok
    && blockingTaskAuditFindings.length === 0
    && failedValidatorRuns.length === 0
    && crossFileConsistencyFindings.length === 0
    && residueReport.blockAndExplain.length === 0
    && residueReport.manualReview.filter((entry) => isActionableManualResidue(entry.path)).length === 0;
  const gitHeadEvidenceRequired = shouldWriteGitHeadEvidenceForStagedCommit({
    stagedFiles,
    criticalChangedFiles: frameworkStatus.criticalChangedFiles
  });
  // TASK-AAO-0136: collect tree-wide advisory findings (warnings only, not blockers).
  const advisoryFindings: PreCommitAdvisoryFinding[] = [...validatorRunTriage.advisoryFindings];
  const teamGateFindings = evaluateTeamPreCommitGate({
    cwd: root,
    actorId: process.env.ATM_COMMIT_ACTOR_ID ?? process.env.ATM_ACTOR_ID ?? null,
    stagedFiles
  }).map((finding): PreCommitBlockingFinding => ({
    code: finding.code,
    source: 'team-runtime-gate',
    detail: finding.detail,
    files: finding.files,
    requiredCommand: finding.requiredCommand,
    classification: 'current-task',
    blockerKind: 'governance-state',
    scope: 'staged',
    data: finding
  }));
  const blockingFindings = [
    ...buildPreCommitBlockingFindings({
    encodingReport,
    gitIndexDiagnostic,
    blockingFrameworkIssues,
    frameworkClaimCommand,
    staleLocks,
    planningMirrorDriftFiles,
    directionLockDriftFiles,
    quickfixDriftFiles,
    quickfixFileLimitExceeded,
    quickfixLineLimitExceeded,
    quickfixChangedLineCount,
    commitAttributionFindings: commitAttributionReport.findings,
    protectedStateFindings: protectedStateReport.findings,
    emergencyUseAuditFindings: emergencyUseAuditReport.findings,
    taskCardStatusFindings: taskCardStatusReport.findings,
    sameFileClaimFindings: sameFileClaimReport.findings,
    taskAuditFindings: taskAudit.findings,
    failedValidatorRuns,
    stagedFiles,
    advisoryFindingsSink: advisoryFindings,
    crossFileConsistencyFindings,
    residueFindings: [
      ...residueReport.blockAndExplain,
      ...residueReport.manualReview.filter((entry) => isActionableManualResidue(entry.path))
    ]
    }),
    ...teamGateFindings
  ];
  const ok = baseOk && teamGateFindings.length === 0;
  const evidenceWrite = ok && gitHeadEvidenceRequired && !scopedIndexActive
    ? writeStagedGitHeadEvidence(root, stagedFiles, commandRuns)
    : null;
  const failureEnvelope = ok
    ? null
    : buildPreCommitFailureEnvelope({
      blockingFindings,
      frameworkClaimCommand,
      gitIndexDiagnostic,
      failedValidatorRuns
    });

  const directionLockAllowedFilesWarning = directionLockAllowedFilesMismatches.length > 0
    ? message('warn', 'ATM_DIRECTION_LOCK_ALLOWED_FILES_MISMATCH', 'Active task direction lock(s) have drifted between top-level files, embedded allowedFiles, or claim.files. Canonical source is taskDirectionLock.allowedFiles.', {
      diagnoses: directionLockAllowedFilesMismatches
    })
    : null;

  return makeResult({
    ok,
    command: 'hook',
    cwd: root,
    messages: [
      ok
        ? message('info', 'ATM_HOOK_PRE_COMMIT_OK', 'ATM pre-commit hook passed and staged git-head evidence when needed.', {
          stagedFileCount: stagedFiles.length,
          criticalChangedFileCount: frameworkStatus.criticalChangedFiles.length,
          gitHeadEvidenceRequired,
          evidencePath: evidenceWrite?.evidencePath ?? null,
          scopedIndexActive,
          advisoryTreeWideFindingsCount: advisoryFindings.length
        })
        : message(
          'error',
          failureEnvelope?.deferredGovernanceCandidate
            ? 'ATM_HOOK_PRE_COMMIT_DEFERRED_GOVERNANCE_REQUIRED'
            : 'ATM_HOOK_PRE_COMMIT_FAILED',
          failureEnvelope?.deferredGovernanceCandidate
            ? 'ATM pre-commit blocked this commit on governance-state requirements; content validation did not report a failure.'
            : 'ATM pre-commit hook blocked this commit.',
          {
          encodingFindings: encodingReport.findings.length,
          frameworkBlockers: blockingFrameworkIssues,
          planningMirrorDriftFiles,
          taskDirectionDriftFiles: directionLockDriftFiles,
          quickfixDriftFiles,
          quickfixFileLimitExceeded,
          quickfixLineLimitExceeded,
          quickfixChangedLineCount,
          protectedStateFindings: protectedStateReport.findings,
          emergencyUseAuditFindings: emergencyUseAuditReport.findings,
          taskCardStatusFindings: taskCardStatusReport.findings,
          sameFileClaimFindings: sameFileClaimReport.findings,
          taskAuditFindings: taskAudit.findings.length,
          failedValidators: failedValidatorRuns.map((entry) => entry.command),
          gitIndexDiagnostic,
          crossFileConsistencyFindings: crossFileConsistencyFindings.length,
          // TASK-AAO-0136: blockingStagedFindings is the operator-facing split.
          blockingStagedFindings: blockingFindings,
          blockingFindings,
          governanceStateFindings: failureEnvelope?.governanceStateFailures ?? [],
          contentValidationFindings: failureEnvelope?.contentValidationFailures ?? [],
          deferredGovernanceCandidate: failureEnvelope?.deferredGovernanceCandidate ?? false,
          advisoryTreeWideFindings: advisoryFindings,
          failureEnvelope,
          nextStep: blockingFindings.find((entry) => entry.requiredCommand)?.requiredCommand ?? frameworkClaimCommand
        }),
      ...(directionLockAllowedFilesWarning ? [directionLockAllowedFilesWarning] : []),
      ...(explicitActorWarning ? [explicitActorWarning] : []),
      ...(autoCleanedResidue.length > 0
        ? [message('warn', 'ATM_HOOK_AUTO_CLEANED_GENERATED_RESIDUE', 'ATM auto-cleaned known generated residue before pre-commit evaluation.', {
            autoCleanedResidue
          })]
        : []),
      ...(advisoryFindings.length > 0
        ? [message('warn', 'ATM_HOOK_PRE_COMMIT_TREE_WIDE_ADVISORY', `Pre-commit found ${advisoryFindings.length} tree-wide task-audit finding(s) that do not affect this commit. Surfaced as advisory only.`, {
            advisoryTreeWideFindings: advisoryFindings
          })]
        : [])
    ],
    evidence: {
      action: 'pre-commit',
      stagedFiles,
      gitIndexDiagnostic,
      encodingReport,
      frameworkStatus,
      allowAdopterInfrastructureSync,
      blockingFrameworkIssues,
      frameworkClaimCommand,
      activeDirectionLocks,
      directionLockPlanningMirrorPaths,
      directionLockAllowedFilesDiagnoses,
      directionLockAllowedFilesMismatches,
      taskOpenImportBundle,
      planningMirrorDriftFiles,
      activeQuickfixLock,
      directionLockDriftFiles,
      quickfixDriftFiles,
      quickfixFileLimitExceeded,
      quickfixLineLimitExceeded,
      quickfixChangedLineCount,
      commitAttributionReport,
      protectedStateReport,
      emergencyUseAuditReport,
      taskCardStatusReport,
      sameFileClaimReport,
      taskAudit,
      commandRuns,
      blockingFindings,
      crossFileConsistencyFindings,
      autoCleanedResidue,
      residueReport,
      failureEnvelope,
      gitHeadEvidenceRequired,
      evidenceWrite
    }
  });
}

export function buildPreCommitBlockingFindings(input: {
  readonly encodingReport: ReturnType<typeof scanEncoding>;
  readonly gitIndexDiagnostic: ReturnType<typeof inspectGitIndexAccess>;
  readonly blockingFrameworkIssues: readonly string[];
  readonly frameworkClaimCommand: string | null;
  readonly staleLocks: readonly FrameworkStaleLockInfo[];
  readonly planningMirrorDriftFiles: readonly string[];
  readonly directionLockDriftFiles: readonly string[];
  readonly quickfixDriftFiles: readonly string[];
  readonly quickfixFileLimitExceeded: boolean;
  readonly quickfixLineLimitExceeded: boolean;
  readonly quickfixChangedLineCount: number;
  readonly commitAttributionFindings: readonly PreCommitBlockingFinding[];
  readonly protectedStateFindings: readonly ProtectedStateFinding[];
  readonly emergencyUseAuditFindings: readonly EmergencyUseAuditFinding[];
  readonly taskCardStatusFindings: readonly TaskCardStatusFinding[];
  readonly sameFileClaimFindings: readonly SameFileClaimOwnershipFinding[];
  readonly taskAuditFindings: ReturnType<typeof auditTasks>['findings'];
  readonly failedValidatorRuns: readonly CommandRunReport[];
  readonly stagedFiles: readonly string[];
  readonly advisoryFindingsSink: PreCommitAdvisoryFinding[];
  readonly crossFileConsistencyFindings: readonly PreCommitBlockingFinding[];
  readonly residueFindings: readonly AutoGeneratedResidueFinding[];
}): readonly PreCommitBlockingFinding[] {
  const findings: PreCommitBlockingFinding[] = [];
  if (!input.gitIndexDiagnostic.ok) {
    findings.push({
      code: input.gitIndexDiagnostic.code,
      source: 'git-index',
      detail: input.gitIndexDiagnostic.detail,
      requiredCommand: input.gitIndexDiagnostic.requiredCommand,
      classification: 'environment',
      data: input.gitIndexDiagnostic
    });
  }
  findings.push(...input.crossFileConsistencyFindings);
  findings.push(...input.commitAttributionFindings);
  for (const finding of input.residueFindings) {
    findings.push({
      code: finding.verdict === 'block-and-explain'
        ? 'ATM_HOOK_GENERATED_RESIDUE_BLOCKED'
        : 'ATM_HOOK_GENERATED_RESIDUE_MANUAL_REVIEW',
      source: 'generated-residue',
      file: finding.path,
      detail: finding.reason,
      classification: 'current-task'
    });
  }
  for (const finding of input.emergencyUseAuditFindings) {
    findings.push({
      code: finding.code,
      source: 'emergency-use-audit',
      file: finding.file,
      detail: finding.detail,
      requiredCommand: null,
      classification: 'current-task',
      data: finding
    });
  }
  for (const finding of input.encodingReport.findings) {
    findings.push({
      code: `ATM_ENCODING_${finding.issue.toUpperCase().replace(/[^A-Z0-9]+/g, '_')}`,
      source: 'encoding',
      file: finding.file,
      detail: `Encoding guard found ${finding.issue} in ${finding.file}.`,
      classification: 'current-task'
    });
  }
  for (const blocker of input.blockingFrameworkIssues) {
    findings.push({
      code: `ATM_FRAMEWORK_${blocker.toUpperCase().replace(/[^A-Z0-9]+/g, '_')}`,
      source: 'framework-development',
      detail: `Framework-development gate blocked this commit: ${blocker}.`,
      requiredCommand: blocker === 'active-framework-claim-required' ? input.frameworkClaimCommand : null,
      classification: 'current-task'
    });
  }
  for (const stale of input.staleLocks) {
    const requiredCommand = isFrameworkStaleLockReleasable(stale)
      ? (input.frameworkClaimCommand ?? buildFrameworkStaleCleanupCommand(stale))
      : stale.requiredCommand;
    findings.push({
      code: 'ATM_FRAMEWORK_STALE_LOCK_CLEANUP_REQUIRED',
      source: 'framework-development',
      detail: stale.detail,
      requiredCommand,
      classification: stale.kind === 'still-active' ? 'blocking' : 'current-task',
      data: {
        kind: stale.kind,
        lockTaskId: stale.lockTaskId,
        lockPath: stale.lockPath,
        linkedTaskId: stale.linkedTaskId,
        currentTaskId: stale.currentTaskId,
        actorId: stale.actorId
      }
    });
  }
  if (input.planningMirrorDriftFiles.length > 0) {
    findings.push({
      code: 'ATM_PLANNING_MIRROR_DRIFT',
      source: 'direction-lock',
      files: input.planningMirrorDriftFiles,
      detail: 'Staged files include planning/mirror paths while the active direction lock allows target work only.',
      classification: 'current-task'
    });
  }
  if (input.directionLockDriftFiles.length > 0) {
    findings.push({
      code: 'ATM_TASK_DIRECTION_SCOPE_DRIFT',
      source: 'direction-lock',
      files: input.directionLockDriftFiles,
      detail: 'Staged files are outside the active task direction lock allowedFiles.',
      classification: 'current-task'
    });
  }
  if (input.quickfixDriftFiles.length > 0) {
    findings.push({
      code: 'ATM_QUICKFIX_SCOPE_DRIFT',
      source: 'quickfix',
      files: input.quickfixDriftFiles,
      detail: 'Staged files are outside the active quickfix allowedFiles.',
      classification: 'current-task'
    });
  }
  if (input.quickfixFileLimitExceeded) {
    findings.push({
      code: 'ATM_QUICKFIX_FILE_LIMIT_EXCEEDED',
      source: 'quickfix',
      detail: 'Quickfix changed too many non-.atm files for the fast channel.',
      classification: 'current-task'
    });
  }
  if (input.quickfixLineLimitExceeded) {
    findings.push({
      code: 'ATM_QUICKFIX_LINE_LIMIT_EXCEEDED',
      source: 'quickfix',
      detail: `Quickfix changed ${input.quickfixChangedLineCount} lines, exceeding the fast-channel line limit.`,
      classification: 'current-task'
    });
  }
  for (const finding of input.protectedStateFindings) {
    findings.push({
      code: `ATM_PROTECTED_STATE_${finding.reason.toUpperCase().replace(/[^A-Z0-9]+/g, '_')}`,
      source: 'protected-atm-state',
      file: finding.file,
      detail: finding.detail,
      requiredCommand: finding.requiredCommand ?? null,
      classification: 'current-task'
    });
  }
  for (const finding of input.taskCardStatusFindings) {
    findings.push({
      code: 'ATM_TASK_CARD_STATUS_DONE_REQUIRES_LEDGER_CLOSURE',
      source: 'task-card-status',
      file: finding.file,
      detail: finding.detail,
      requiredCommand: finding.requiredCommand,
      classification: 'current-task',
      data: finding
    });
  }
  for (const finding of input.sameFileClaimFindings) {
    findings.push({
      code: finding.code,
      source: 'same-file-claim-ownership',
      file: finding.file,
      detail: finding.detail,
      requiredCommand: finding.requiredCommand,
      classification: 'current-task',
      data: finding
    });
  }
  // TASK-AAO-0136: scope-classify task-audit findings.
  //   staged   -> blocking (current-task) — the author's commit ships the offending change
  //   tree-wide -> advisory warning — pre-existing tree state, do not block other commits
  // INV-ATM-* invariant violations still block regardless of scope.
  const stagedSet = new Set(input.stagedFiles);
  for (const finding of input.taskAuditFindings.filter((entry) => entry.level === 'error')) {
    const findingPath = 'path' in finding && typeof finding.path === 'string' ? finding.path : undefined;
    const isInvariantViolation = INVARIANT_TASK_AUDIT_CODES.has(finding.code);
    const isStaged = findingPath ? stagedSet.has(findingPath) : false;
    const scope: 'staged' | 'tree-wide' = isStaged ? 'staged' : 'tree-wide';
    if (scope === 'staged' || isInvariantViolation) {
      findings.push({
        code: finding.code,
        source: 'task-audit',
        file: findingPath,
        detail: finding.detail,
        classification: 'current-task',
        scope
      });
    } else {
      input.advisoryFindingsSink.push({
        code: finding.code,
        source: 'task-audit',
        file: findingPath,
        detail: finding.detail,
        scope: 'tree-wide',
        taskId: 'taskId' in finding && typeof finding.taskId === 'string' ? finding.taskId : undefined
      });
    }
  }
  for (const run of input.failedValidatorRuns) {
    findings.push({
      code: 'ATM_FRAMEWORK_VALIDATOR_FAILED',
      source: 'framework-validator',
      detail: `${run.command} exited with ${run.exitCode}.`,
      classification: 'current-task',
      data: {
        command: run.command,
        exitCode: run.exitCode,
        stdoutSha256: run.stdoutSha256,
        stderrSha256: run.stderrSha256
      }
    });
  }
  return findings;
}

export function buildPreCommitFailureEnvelope(input: {
  readonly blockingFindings: readonly PreCommitBlockingFinding[];
  readonly frameworkClaimCommand: string | null;
  readonly gitIndexDiagnostic: ReturnType<typeof inspectGitIndexAccess>;
  readonly failedValidatorRuns: readonly CommandRunReport[];
}): PreCommitFailureEnvelope {
  const requiredCommand = input.blockingFindings.find((entry) => entry.requiredCommand)?.requiredCommand
    ?? input.frameworkClaimCommand
    ?? null;
  const baselineFailures = input.blockingFindings.filter(isPreCommitBaselineFinding);
  const currentTaskFailures = input.blockingFindings.filter((finding) => !isPreCommitBaselineFinding(finding) && !isPreCommitEnvironmentFinding(finding));
  const governanceStateFailures = currentTaskFailures.filter(isPreCommitGovernanceStateFinding);
  const contentValidationFailures = currentTaskFailures.filter((finding) => !isPreCommitGovernanceStateFinding(finding));
  return {
    schemaId: 'atm.validatorFailureEnvelope.v1',
    ok: false,
    surface: 'pre-commit',
    requiredCommand,
    blockingFindings: input.blockingFindings,
    baselineFailures,
    currentTaskFailures,
    governanceStateFailures,
    contentValidationFailures,
    deferredGovernanceCandidate: governanceStateFailures.length > 0 && contentValidationFailures.length === 0,
    repairHints: buildPreCommitRepairHints(input.blockingFindings, requiredCommand),
    diagnostics: {
      gitIndexDiagnostic: input.gitIndexDiagnostic,
      failedValidators: input.failedValidatorRuns.map((entry) => ({
        command: entry.command,
        exitCode: entry.exitCode,
        stdoutSha256: entry.stdoutSha256,
        stderrSha256: entry.stderrSha256
      }))
    }
  };
}

export function buildPreCommitRepairHints(
  findings: readonly PreCommitBlockingFinding[],
  requiredCommand: string | null
): readonly string[] {
  if (findings.length === 0) {
    return requiredCommand ? [`Run required command: ${requiredCommand}`] : [];
  }
  return findings.map((finding) => {
    if (finding.code === 'ATM_ENV_SANDBOX_GIT_EPERM') {
      return 'Rerun the commit with repository-level Git permissions, or set ATM_TEMP_ROOT=C:\\tmp for validators that create temporary Git repositories.';
    }
    if (finding.code === 'ATM_GIT_INDEX_PERMISSION_DENIED') {
      return 'Resolve the local Git/index permission problem outside ATM, then retry the commit. This is an environment diagnostic, not task evidence.';
    }
    if (finding.requiredCommand) {
      return `Run required command: ${finding.requiredCommand}`;
    }
    return `Resolve ${finding.source} finding ${finding.code}, then retry the commit.`;
  });
}

export function isPreCommitBaselineFinding(finding: PreCommitBlockingFinding): boolean {
  return finding.classification === 'baseline' || finding.source === 'baseline';
}

export function isPreCommitEnvironmentFinding(finding: PreCommitBlockingFinding): boolean {
  return finding.classification === 'environment'
    || finding.source === 'environment'
    || finding.source === 'git-index'
    || finding.code.startsWith('ATM_ENV_')
    || finding.code.startsWith('ATM_GIT_INDEX_');
}

function isPreCommitGovernanceStateFinding(finding: PreCommitBlockingFinding): boolean {
  if (finding.blockerKind) {
    return finding.blockerKind === 'governance-state';
  }
  return finding.source === 'framework-development'
    || finding.source === 'direction-lock'
    || finding.source === 'quickfix'
    || finding.source === 'protected-atm-state'
    || finding.source === 'same-file-claim-ownership'
    || finding.source === 'generated-residue'
    || finding.source === 'emergency-use-audit';
}

function readStagedFiles(cwd: string): readonly string[] {
  return uniqueSorted(runGitLines(cwd, ['diff', '--cached', '--name-only', '--diff-filter=ACMRT'])
    .map(normalizeRelativePath)
    .filter(Boolean));
}

function readStagedChangedLineCount(cwd: string, files: readonly string[]) {
  if (files.length === 0) return 0;
  const lines = runGitLines(cwd, ['diff', '--cached', '--numstat', '--', ...files]);
  let total = 0;
  for (const line of lines) {
    const [added, deleted] = line.split('\t');
    const addedCount = Number.parseInt(added, 10);
    const deletedCount = Number.parseInt(deleted, 10);
    if (Number.isFinite(addedCount)) total += addedCount;
    if (Number.isFinite(deletedCount)) total += deletedCount;
  }
  return total;
}

function scanEncoding(cwd: string, files: readonly string[]) {
  const findings: Array<{ readonly file: string; readonly issue: string }> = [];
  for (const file of files) {
    if (!isTextFile(file)) continue;
    const absolutePath = path.join(cwd, file);
    if (!existsSync(absolutePath)) continue;
    const buffer = readFileSync(absolutePath);
    const text = buffer.toString('utf8');
    if (buffer.length >= 3 && buffer[0] === 0xef && buffer[1] === 0xbb && buffer[2] === 0xbf) {
      findings.push({ file, issue: 'utf8-bom' });
    }
    if (text.includes('\uFFFD')) {
      findings.push({ file, issue: 'replacement-character' });
    }
    if (/[\u00c3\u00e2\u00e5].|\u749d.|\u7587.|\u765f./.test(text)) {
      findings.push({ file, issue: 'possible-mojibake' });
    }
  }
  return {
    schemaId: 'atm.encodingHookReport.v1',
    inspectedFileCount: files.filter(isTextFile).length,
    findings,
    ok: findings.length === 0
  };
}

function inspectTaskCardStatusChanges(cwd: string, stagedFiles: readonly string[]) {
  const findings: TaskCardStatusFinding[] = [];
  for (const file of stagedFiles) {
    if (!isTaskCardMarkdownPath(file)) continue;
    const stagedText = readGitObjectText(cwd, `:${file}`);
    if (!stagedText) continue;
    const nextStatus = parseMarkdownTaskCardStatus(stagedText);
    if (!isDoneLikeTaskCardStatus(nextStatus)) continue;
    const headText = readGitObjectText(cwd, `HEAD:${file}`);
    const previousStatus = headText ? parseMarkdownTaskCardStatus(headText) : null;
    if (isDoneLikeTaskCardStatus(previousStatus)) continue;
    const taskId = parseMarkdownTaskCardId(stagedText, file);
    if (hasLocalLedgerClosure(cwd, taskId) || hasClosureSyncAttestation(stagedText)) continue;
    findings.push({
      file,
      taskId,
      previousStatus,
      nextStatus: nextStatus ?? 'done',
      reason: 'planning-card-done-without-ledger-closure',
      detail: `Task card ${file} changes status to done, but ATM could not verify a matching task ledger closure packet. Planning cards are mirrors; close the task through ATM before syncing status.`,
      requiredCommand: `node atm.mjs next --prompt ${JSON.stringify(taskId)} --json`
    });
  }
  return {
    schemaId: 'atm.taskCardStatusPreCommitReport.v1',
    inspectedFileCount: stagedFiles.filter(isTaskCardMarkdownPath).length,
    findings,
    ok: findings.length === 0
  };
}

function isTaskCardMarkdownPath(file: string) {
  return normalizeRelativePath(file).toLowerCase().endsWith('.task.md');
}

function parseMarkdownTaskCardStatus(text: string): string | null {
  const frontmatter = /^---\r?\n([\s\S]*?)\r?\n---/.exec(text)?.[1] ?? text.slice(0, 2000);
  const match = /^status:\s*['"]?([^'"\r\n#]+)['"]?\s*$/im.exec(frontmatter);
  return match ? match[1].trim().toLowerCase() : null;
}

function parseMarkdownTaskCardId(text: string, file: string): string {
  const frontmatter = /^---\r?\n([\s\S]*?)\r?\n---/.exec(text)?.[1] ?? text.slice(0, 2000);
  const match = /^(?:task_id|taskId|workItemId|id):\s*['"]?([^'"\r\n#]+)['"]?\s*$/im.exec(frontmatter);
  const fallback = path.basename(file).replace(/\.task\.md$/i, '');
  return (match?.[1]?.trim() || fallback).toUpperCase();
}

function isDoneLikeTaskCardStatus(status: string | null) {
  return status === 'done' || status === 'verified';
}

function hasLocalLedgerClosure(cwd: string, taskId: string) {
  const taskPath = path.join(cwd, '.atm', 'history', 'tasks', `${taskId}.json`);
  if (!existsSync(taskPath)) return false;
  try {
    const parsed = readJsonText(readFileSync(taskPath, 'utf8')) as Record<string, unknown> | null;
    if (!parsed || !isDoneLikeTaskCardStatus(normalizeOptionalText(parsed.status)?.toLowerCase() ?? null)) return false;
    const closurePacket = normalizeOptionalText(parsed.closurePacket ?? parsed.closure_packet);
    if (!closurePacket) return false;
    return existsSync(path.join(cwd, closurePacket));
  } catch {
    return false;
  }
}

function hasClosureSyncAttestation(text: string) {
  return /Closure sync:/i.test(text)
    && /\.closure-packet\.json/i.test(text)
    && /\b[0-9a-f]{7,40}\b/i.test(text);
}

