// TASK-CID-0024: decide staged ownership when multiple active claims may cover
// the same file. Multiple same-file claims alone never block; a finding is
// produced only when:
//   1. the staged file is covered by active write claims that do NOT include
//      the committing task (mixed staged content) and no steward/broker
//      evidence covers the file, or
//   2. the committing task claimed closeout-only / no-more-mutation but stages
//      real source mutations anyway (the intent is non-mutating by contract).
function inspectSameFileClaimOwnership(input: {
  readonly cwd: string;
  readonly stagedFiles: readonly string[];
  readonly activeDirectionLocks: ReturnType<typeof readActiveTaskDirectionLocks>;
  readonly exemptAllowedFileSets: readonly (readonly string[])[];
}): SameFileClaimOwnershipReport {
  const inferredTaskIds = inferTaskIdsFromStagedFiles(input.stagedFiles);
  const committingTaskId = normalizeOptionalText(process.env.ATM_COMMIT_TASK_ID)
    ?? (inferredTaskIds.length === 1 ? inferredTaskIds[0] : null);
  const claimIntentByTaskId = new Map<string, string>();
  const readClaimIntent = (taskId: string): string => {
    const cached = claimIntentByTaskId.get(taskId);
    if (cached) return cached;
    const taskDocument = readJsonFile(path.join(input.cwd, '.atm', 'history', 'tasks', `${taskId}.json`));
    const claim = taskDocument?.claim && typeof taskDocument.claim === 'object' && !Array.isArray(taskDocument.claim)
      ? taskDocument.claim as Record<string, unknown>
      : null;
    const intent = normalizeOptionalText(claim?.intent) ?? 'write';
    claimIntentByTaskId.set(taskId, intent);
    return intent;
  };
  const writeLocks = input.activeDirectionLocks.filter((lock) => readClaimIntent(lock.taskId) !== 'closeout-only');
  const committingClaimIntent = committingTaskId ? readClaimIntent(committingTaskId) : null;
  const stewardCoveredFiles = collectStewardBrokerCoveredFiles(input.cwd);
  const stewardCoveredSet = new Set(stewardCoveredFiles.map((entry) => normalizeRelativePath(entry).toLowerCase()));
  const findings: SameFileClaimOwnershipFinding[] = [];
  const multiClaimFiles: { file: string; writeClaimTaskIds: readonly string[] }[] = [];
  for (const stagedFile of input.stagedFiles) {
    const normalized = normalizeRelativePath(stagedFile);
    if (!normalized || normalized.startsWith('.atm/')) continue;
    if (isTaskDirectionPreCommitExempt(normalized)) continue;
    if (input.exemptAllowedFileSets.some((allowed) => allowed.length > 0 && isPathAllowedByTaskDirection(normalized, allowed))) continue;
    const coveringWriteLocks = writeLocks.filter((lock) => isPathAllowedByTaskDirection(normalized, lock.allowedFiles));
    if (coveringWriteLocks.length === 0) {
      // No active write claim covers this file; direction-lock drift owns it.
      // Still enforce the closeout-only non-mutation contract for the
      // committing task itself.
      if (committingClaimIntent === 'closeout-only'
        && input.activeDirectionLocks.some((lock) => lock.taskId === committingTaskId && isPathAllowedByTaskDirection(normalized, lock.allowedFiles))) {
        findings.push({
          code: 'ATM_PRE_COMMIT_CLOSEOUT_ONLY_CLAIM_MUTATION',
          file: normalized,
          committingTaskId,
          writeClaimTaskIds: [],
          detail: `Task ${committingTaskId} holds a closeout-only / no-more-mutation claim but stages source mutation ${normalized}. Re-claim with a write intent before shipping new source changes.`,
          requiredCommand: committingTaskId
            ? `node atm.mjs next --claim --actor <id> --task ${committingTaskId} --claim-intent write --json`
            : null
        });
      }
      continue;
    }
    const writeClaimTaskIds = uniqueSorted(coveringWriteLocks.map((lock) => lock.taskId));
    if (writeClaimTaskIds.length > 1) {
      multiClaimFiles.push({ file: normalized, writeClaimTaskIds });
    }
    const committingOwnsFile = Boolean(committingTaskId) && writeClaimTaskIds.includes(committingTaskId as string);
    if (committingOwnsFile) continue;
    const ambiguous = committingTaskId ? writeClaimTaskIds.length >= 1 : writeClaimTaskIds.length >= 2;
    if (!ambiguous) continue;
    if (stewardCoveredSet.has(normalized.toLowerCase())) continue;
    findings.push({
      code: 'ATM_PRE_COMMIT_STAGED_OWNERSHIP_AMBIGUOUS',
      file: normalized,
      committingTaskId,
      writeClaimTaskIds,
      detail: committingTaskId
        ? `Staged file ${normalized} belongs to active write claim(s) ${writeClaimTaskIds.join(', ')} but the committing task ${committingTaskId} does not own it, and no steward/broker evidence covers it. Remove it from this commit or route it through the steward lane.`
        : `Staged file ${normalized} is covered by multiple active write claims (${writeClaimTaskIds.join(', ')}) and ATM cannot prove which task owns this commit. Commit through node atm.mjs git commit --task <id> or provide steward/broker evidence.`,
      requiredCommand: 'node atm.mjs git commit --actor <id> --task <task> --message "<summary>" --json'
    });
  }
  return {
    ok: findings.length === 0,
    committingTaskId,
    committingClaimIntent,
    multiClaimFiles,
    stewardCoveredFiles,
    findings
  };
}

function selectRelevantDirectionLocksForCommit(input: {
  readonly activeDirectionLocks: ReturnType<typeof readActiveTaskDirectionLocks>;
  readonly stagedFiles: readonly string[];
  readonly committingTaskId: string | null;
  readonly taskGovernedCommitAllowedFiles: readonly string[];
  readonly closeCommitWindowAllowedFiles: readonly string[];
  readonly closeCommitWindowPlanningMirrorFiles: readonly string[];
}): ReturnType<typeof readActiveTaskDirectionLocks> {
  const currentTaskAllowedFiles = uniqueSorted([
    ...input.taskGovernedCommitAllowedFiles,
    ...input.closeCommitWindowAllowedFiles,
    ...input.closeCommitWindowPlanningMirrorFiles
  ]);
  return input.activeDirectionLocks.filter((lock) => {
    if (input.committingTaskId && taskIdsEqual(lock.taskId, input.committingTaskId)) {
      return true;
    }
    return input.stagedFiles.some((entry) => {
      if (isTaskDirectionPreCommitExempt(entry)) return false;
      if (currentTaskAllowedFiles.length > 0 && isPathAllowedByTaskDirection(entry, currentTaskAllowedFiles)) {
        return false;
      }
      return isPathAllowedByTaskDirection(entry, lock.allowedFiles)
        || isPlanningMirrorPath(entry, lock.planningMirrorPaths ?? []);
    });
  });
}

// TASK-CID-0024: files currently covered by an active steward/composer broker
// intent count as steward/broker evidence for staged ownership decisions.
function collectStewardBrokerCoveredFiles(cwd: string): readonly string[] {
  try {
    const state = readBrokerLifecycleState(cwd);
    return uniqueSorted(state.activeIntents
      .filter((intent) => intent.lane === 'neutral-steward' || intent.lane === 'deterministic-composer')
      .flatMap((intent) => intent.resourceKeys?.files ?? [])
      .map((entry) => normalizeRelativePath(entry))
      .filter(Boolean));
  } catch {
    return [];
  }
}

function isTaskDirectionPreCommitExempt(value: string): boolean {
  const normalized = normalizeRelativePath(value).toLowerCase();
  return normalized.startsWith('.atm/history/task-events/')
    || normalized.startsWith('.atm/history/evidence/')
    || normalized.startsWith('.atm/runtime/locks/')
    || normalized.startsWith('.atm/runtime/task-queues/')
    || normalized.startsWith('.atm/runtime/batch-runs/')
    || normalized.startsWith('.atm/runtime/task-direction-locks/');
}

function collectStagedBatchCheckpointScopeFiles(cwd: string, stagedFiles: readonly string[]): readonly string[] {
  const stagedSet = new Set(stagedFiles.map((entry) => normalizeRelativePath(entry)));
  const allowedFiles: string[] = [];
  for (const file of stagedFiles) {
    const normalized = normalizeRelativePath(file);
    const lower = normalized.toLowerCase();
    if (!lower.startsWith('.atm/history/tasks/') || !lower.endsWith('.json')) {
      continue;
    }
    const task = readJsonFile(path.join(cwd, normalized));
    if (task?.status !== 'done') continue;
    const taskId = typeof task.workItemId === 'string' ? task.workItemId : path.basename(normalized, '.json');
    const lastTransitionId = typeof task.lastTransitionId === 'string' ? task.lastTransitionId : '';
    const expectedEventPath = `.atm/history/task-events/${taskId}/${lastTransitionId}.json`;
    if (!lastTransitionId || !stagedSet.has(expectedEventPath)) {
      continue;
    }
    const event = readJsonFile(path.join(cwd, expectedEventPath));
    const closure = event?.closure as { schemaId?: unknown } | undefined;
    if (typeof event?.command !== 'string'
      || !event.command.startsWith('node atm.mjs tasks close')
      || (!event.command.includes('--from-batch-checkpoint') && closure?.schemaId !== 'atm.taskClosureTransition.v1')) {
      continue;
    }
    allowedFiles.push(normalized);
    allowedFiles.push(...extractCheckpointTaskScopeFiles(task));
  }
  return uniqueSorted(allowedFiles);
}

function collectFrameworkTempClaimAllowedFiles(cwd: string): readonly string[] {
  const lockRoot = path.join(cwd, '.atm', 'runtime', 'locks');
  if (!existsSync(lockRoot)) return [];
  const allowedFiles: string[] = [];
  for (const entry of readdirSync(lockRoot).filter((fileName) => fileName.startsWith('ATM-FRAMEWORK-TEMP-') && fileName.endsWith('.lock.json'))) {
    const lock = readJsonFile(path.join(lockRoot, entry));
    collectStringArrayField(lock?.files, allowedFiles);
  }
  return uniqueSorted(allowedFiles.map(normalizeRelativePath).filter(isTaskDirectionPathCandidate));
}

function collectCloseCommitWindowPlanningMirrorFiles(cwd: string): readonly string[] {
  const files: string[] = [];
  for (const win of readActiveCloseCommitWindows(cwd)) {
    const task = readJsonFile(path.join(cwd, '.atm', 'history', 'tasks', `${win.taskId}.json`));
    if (!task || typeof task !== 'object') continue;
    const source = isPlainObject(task.source) ? task.source : null;
    const planPath = typeof source?.planPath === 'string' ? normalizeRelativePath(source.planPath) : '';
    if (planPath) files.push(planPath);
    collectStringArrayField((task as Record<string, unknown>).planningMirrorPaths, files);
  }
  return uniqueSorted(files.map(normalizeRelativePath).filter(isTaskDirectionPathCandidate));
}

function collectTaskGovernedCommitAllowedFiles(cwd: string, taskId: string | null): readonly string[] {
  if (!taskId) return [];
  const files = [
    `.atm/history/tasks/${taskId}.json`,
    `.atm/history/evidence/${taskId}.json`,
    `.atm/history/evidence/${taskId}.bundle-manifest.json`,
    `.atm/history/evidence/${taskId}.closure-packet.json`,
    `.atm/history/task-events/${taskId}/**`
  ];
  const task = readJsonFile(path.join(cwd, '.atm', 'history', 'tasks', `${taskId}.json`));
  if (task && typeof task === 'object') {
    const source = isPlainObject(task.source) ? task.source : null;
    const planPath = typeof source?.planPath === 'string' ? normalizeRelativePath(source.planPath) : '';
    if (planPath) files.push(planPath);
    collectStringArrayField((task as Record<string, unknown>).planningMirrorPaths, files);
  }
  const actorRegistryState = inspectTrackedActorRegistryState(cwd);
  if (actorRegistryState.tracked && (actorRegistryState.staged || actorRegistryState.unstaged)) {
    files.push(actorRegistryState.path);
  }
  return uniqueSorted(files.map(normalizeRelativePath).filter(isTaskDirectionPathCandidate));
}

function extractCheckpointTaskScopeFiles(task: Record<string, unknown>): readonly string[] {
  const candidates: string[] = [];
  collectStringArrayField(task.scope, candidates);
  collectStringArrayField(task.scopePaths, candidates);
  collectStringArrayField(task.deliverables, candidates);
  collectStringArrayField(task.files, candidates);
  collectStringArrayField(task.allowedFiles, candidates);
  const targetWork = isPlainObject(task.targetWork) ? task.targetWork : null;
  if (targetWork) {
    collectStringArrayField(targetWork.allowedFiles, candidates);
    collectStringArrayField(targetWork.files, candidates);
  }
  return uniqueSorted(candidates
    .map(normalizeRelativePath)
    .filter(isTaskDirectionPathCandidate));
}

function collectStringArrayField(value: unknown, output: string[]) {
  if (!Array.isArray(value)) return;
  for (const entry of value) {
    if (typeof entry === 'string') output.push(entry);
  }
}

function isPlainObject(value: unknown): value is Record<string, unknown> {
  return typeof value === 'object' && value !== null && !Array.isArray(value);
}

export function inspectProtectedAtmStateChanges(cwd: string, stagedFiles: readonly string[]) {
  const protectedFiles = stagedFiles.filter((entry) => isProtectedAtmManagedStatePath(entry) || isStaticEvidenceArtifactPath(entry));
  const findings: ProtectedStateFinding[] = [];
  const activeBatches = listActiveBatchRuns(cwd);
  const stagedBatch = activeBatches.find((batchRun) => batchRun.taskIds.some((taskId) => protectedFiles.some((file) => normalizeRelativePath(file).toLowerCase() === `.atm/history/tasks/${taskId.toLowerCase()}.json`))) ?? null;
  const pendingBatchCheckpointWindow = activeBatches
    .map((batchRun) => buildPendingCheckpointCommitWindow(cwd, batchRun, null))
    .find((window) => window && protectedFiles.every((file) => isPathAllowedByScope(file, uniqueSorted([...window.commitFiles, ...window.changedFiles])))) ?? null;
  const batchDeliverAndCloseCommit = process.env.ATM_BATCH_DELIVER_AND_CLOSE === '1';
  const nonAtmStagedFiles = stagedFiles
    .map((entry) => normalizeRelativePath(entry))
    .filter((entry) => !entry.toLowerCase().startsWith('.atm/'));
  if (stagedBatch?.status === 'active'
    && !batchDeliverAndCloseCommit
    && nonAtmStagedFiles.length > 0
    && !hasStagedBatchCheckpointClosure(cwd, protectedFiles, stagedBatch.taskIds, stagedBatch.batchId)) {
    const requiredCommand = `node atm.mjs batch checkpoint --actor <id> --batch ${stagedBatch.batchId} --json`;
    findings.push({
      file: nonAtmStagedFiles[0] ?? '<staged-files>',
      reason: 'batch-commit-before-checkpoint',
      detail: `Active batch ${stagedBatch.batchId} has not checkpointed the staged deliverable commit. Run ${requiredCommand} first, then commit the deliverables together with the checkpoint task/evidence/events.`,
      requiredCommand
    });
  }
  if (protectedFiles.length === 0) {
    return {
      ok: findings.length === 0,
      files: [] as readonly string[],
      findings
    };
  }

  const stagedTaskIds = inferTaskIdsFromStagedFiles(stagedFiles);
  if (stagedTaskIds.length === 1 && inspectHistoricalLedgerRestoreStagedArtifacts(cwd, stagedTaskIds[0], stagedFiles).ok) {
    return {
      ok: findings.length === 0,
      files: protectedFiles,
      findings
    };
  }

  const stagedSet = new Set(protectedFiles.map((entry) => normalizeRelativePath(entry)));
  const singleStagedTaskId = stagedTaskIds.length === 1 ? stagedTaskIds[0] : null;
  const effectiveSingleStagedTaskId = singleStagedTaskId ?? pendingBatchCheckpointWindow?.taskId ?? null;
  const pendingBatchCheckpointAllowedFiles = pendingBatchCheckpointWindow
    ? uniqueSorted([...pendingBatchCheckpointWindow.commitFiles, ...pendingBatchCheckpointWindow.changedFiles])
    : [];

  for (const file of protectedFiles) {
    const normalized = normalizeRelativePath(file);
    const lower = normalized.toLowerCase();
    const absolutePath = path.join(cwd, normalized);

    if (isProtectedAtmRuntimeStatePath(normalized)) {
      findings.push({
        file: normalized,
        reason: 'runtime-state-must-not-be-committed',
        detail: 'Runtime lock, queue, or active-session state must stay ephemeral and must not be committed.'
      });
      continue;
    }

    if (lower.startsWith('.atm/history/task-events/')) {
      const event = readJsonFile(absolutePath);
      const command = typeof event?.command === 'string' ? event.command.trim() : '';
      if (event?.schemaId !== 'atm.taskTransition.v1' || typeof event?.transitionId !== 'string' || typeof event?.taskId !== 'string') {
        findings.push({
          file: normalized,
          reason: 'task-transition-json-invalid',
          detail: 'Task transition event is missing atm.taskTransition.v1 metadata.'
        });
        continue;
      }
      if (!command.startsWith('node atm.mjs ')) {
        findings.push({
          file: normalized,
          reason: 'task-transition-command-invalid',
          detail: 'Task transition event does not record a node atm.mjs command as its source.'
        });
      }
      continue;
    }

    if (lower.startsWith('.atm/history/tasks/')) {
      const task = readJsonFile(absolutePath);
      const taskId = typeof task?.workItemId === 'string' ? task.workItemId : path.basename(normalized, '.json');
      const lastTransitionId = typeof task?.lastTransitionId === 'string' ? task.lastTransitionId : '';
      const expectedEventPath = `.atm/history/task-events/${taskId}/${lastTransitionId}.json`;
      const stagedEventPath = lastTransitionId
        ? findCaseInsensitiveRelativePath(stagedSet, expectedEventPath)
        : null;
      if (!lastTransitionId || !stagedEventPath) {
        findings.push({
          file: normalized,
          reason: 'task-file-missing-transition',
          detail: 'Task ledger change must be committed with its matching staged task transition event.'
        });
        continue;
      }
      const event = readJsonFile(path.join(cwd, stagedEventPath));
      const expectedSha = sha256(readFileSync(absolutePath));
      const owningBatch = activeBatches.find((batchRun) => taskIdsInclude(batchRun.taskIds, taskId)) ?? null;
      if (event?.schemaId !== 'atm.taskTransition.v1'
        || event?.transitionId !== lastTransitionId
        || typeof event?.taskId !== 'string'
        || !taskIdsEqual(event.taskId, taskId)
        || event?.taskPath !== normalized
        || event?.taskSha256 !== expectedSha
        || typeof event?.command !== 'string'
        || !event.command.startsWith('node atm.mjs ')) {
        findings.push({
          file: normalized,
          reason: 'task-file-transition-mismatch',
          detail: 'Task ledger change does not match a valid staged ATM CLI transition event.'
        });
      } else if (owningBatch?.status === 'active'
        && typeof event?.command === 'string'
        && event.command.startsWith('node atm.mjs tasks close')
        && !event.command.includes('--from-batch-checkpoint')
        && (event?.closure as { schemaId?: unknown } | undefined)?.schemaId !== 'atm.taskClosureTransition.v1') {
        const requiredCommand = `node atm.mjs batch checkpoint --actor <id> --batch ${owningBatch.batchId} --json`;
        findings.push({
          file: normalized,
          reason: 'batch-close-must-use-checkpoint',
          detail: `Task ${taskId} belongs to active batch ${owningBatch.batchId}; direct tasks close is not allowed. Use batch checkpoint so ATM can close, advance, and claim the next queue head.`,
          requiredCommand
        });
      }
      continue;
    }

    if (lower.startsWith('.atm/history/evidence/')) {
      const evidence = readJsonFile(absolutePath);
      if (lower.startsWith('.atm/history/evidence/historical-batches/')) {
        const envelopeTasks = Array.isArray(evidence?.tasks) ? evidence.tasks : [];
        const taskIds = envelopeTasks
          .map((entry) => entry && typeof entry === 'object' && !Array.isArray(entry) ? (entry as Record<string, unknown>).taskId : null)
          .filter((entry): entry is string => typeof entry === 'string' && entry.trim().length > 0);
        const hasBatchTaskContext = taskIds.some((taskId) => {
          const normalizedTaskId = taskId.toLowerCase();
          const hasSiblingTask = stagedSet.has(`.atm/history/tasks/${taskId}.json`);
          const hasSiblingEvent = protectedFiles.some((entry) => {
            const candidate = normalizeRelativePath(entry).toLowerCase();
            return candidate.startsWith(`.atm/history/task-events/${normalizedTaskId}/`);
          });
          return hasSiblingTask || hasSiblingEvent;
        });
        if (taskIds.length === 0 || !hasBatchTaskContext) {
          findings.push({
            file: normalized,
            reason: 'evidence-file-missing-task-context',
            detail: 'Historical batch evidence updates must travel with at least one staged task ledger change or transition event referenced by the batch envelope.'
          });
        }
        continue;
      }
      if (pendingBatchCheckpointAllowedFiles.length > 0 && isPathAllowedByScope(normalized, pendingBatchCheckpointAllowedFiles)) {
        continue;
      }
      if (effectiveSingleStagedTaskId && isNestedEvidenceArtifactPath(normalized)) {
        const taskIdLower = effectiveSingleStagedTaskId.toLowerCase();
        const hasSiblingTask = stagedSet.has(`.atm/history/tasks/${effectiveSingleStagedTaskId}.json`);
        const hasSiblingEvent = protectedFiles.some((entry) => {
          const candidate = normalizeRelativePath(entry).toLowerCase();
          return candidate.startsWith(`.atm/history/task-events/${taskIdLower}/`);
        });
        const hasSiblingTaskEvidence = stagedSet.has(`.atm/history/evidence/${effectiveSingleStagedTaskId}.json`);
        if (hasSiblingTask || hasSiblingEvent || hasSiblingTaskEvidence) {
          continue;
        }
      }
      const taskId = typeof evidence?.taskId === 'string' ? evidence.taskId : path.basename(normalized, '.json');
      const hasSiblingTask = stagedSet.has(`.atm/history/tasks/${taskId}.json`);
      const hasSiblingEvent = protectedFiles.some((entry) => {
        const candidate = normalizeRelativePath(entry).toLowerCase();
        return candidate.startsWith(`.atm/history/task-events/${taskId.toLowerCase()}/`);
      });
      if (!hasSiblingTask && !hasSiblingEvent) {
        findings.push({
          file: normalized,
          reason: 'evidence-file-missing-task-context',
          detail: 'Evidence updates must travel with the related staged task ledger change or transition event.'
        });
      }
    }

    if (isStaticEvidenceArtifactPath(normalized)) {
      const hasSiblingEvidence = protectedFiles.some((entry) => {
        const candidate = normalizeRelativePath(entry).toLowerCase();
        return candidate.startsWith('.atm/history/evidence/');
      });
      const hasSiblingTaskOrEvent = protectedFiles.some((entry) => {
        const candidate = normalizeRelativePath(entry).toLowerCase();
        return candidate.startsWith('.atm/history/tasks/') || candidate.startsWith('.atm/history/task-events/');
      });
      if (!hasSiblingEvidence || !hasSiblingTaskOrEvent) {
        findings.push({
          file: normalized,
          reason: 'static-evidence-artifact-without-cli-context',
          detail: 'Static evidence artifacts under atomic_workbench/evidence or atomic_workbench/reports cannot stand alone; commit them together with ATM CLI evidence/task transition context.'
        });
      }
    }
  }

  return {
    ok: findings.length === 0,
    files: protectedFiles,
    findings
  };
}

function isNestedEvidenceArtifactPath(value: string): boolean {
  const normalized = normalizeRelativePath(value).toLowerCase();
  if (!normalized.startsWith('.atm/history/evidence/')) return false;
  const relative = normalized.slice('.atm/history/evidence/'.length);
  return relative.includes('/');
}

function hasStagedBatchCheckpointClosure(cwd: string, protectedFiles: readonly string[], batchTaskIds: readonly string[], batchId: string | null = null) {
  const protectedSet = new Set(protectedFiles.map((entry) => normalizeRelativePath(entry)));
  for (const file of protectedFiles) {
    const normalized = normalizeRelativePath(file);
    const lower = normalized.toLowerCase();
    if (!lower.startsWith('.atm/history/tasks/') || !lower.endsWith('.json')) {
      continue;
    }
    const task = readJsonFile(path.join(cwd, normalized));
    const taskId = typeof task?.workItemId === 'string' ? task.workItemId : path.basename(normalized, '.json');
    if (!batchTaskIds.includes(taskId) || task?.status !== 'done') {
      continue;
    }
    const lastTransitionId = typeof task?.lastTransitionId === 'string' ? task.lastTransitionId : '';
    const expectedEventPath = `.atm/history/task-events/${taskId}/${lastTransitionId}.json`;
    if (!lastTransitionId || !protectedSet.has(expectedEventPath)) {
      continue;
    }
    const event = readJsonFile(path.join(cwd, expectedEventPath));
    const closure = event?.closure as { schemaId?: unknown; batchId?: unknown } | undefined;
    if (typeof event?.command === 'string'
      && event.command.startsWith('node atm.mjs tasks close')
      && (event.command.includes('--from-batch-checkpoint') || closure?.schemaId === 'atm.taskClosureTransition.v1')
      && (!batchId || event.command.includes(`--batch ${batchId}`) || closure?.batchId === batchId)) {
      return true;
    }
  }
  return false;
}

function isProtectedAtmManagedStatePath(value: string): boolean {
  const normalized = normalizeRelativePath(value).toLowerCase();
  return normalized.startsWith('.atm/history/tasks/')
    || normalized.startsWith('.atm/history/task-events/')
    || normalized.startsWith('.atm/history/evidence/')
    || isProtectedAtmRuntimeStatePath(normalized);
}

function inspectCommitAttribution(cwd: string, stagedFiles: readonly string[]): {
  readonly ok: boolean;
  readonly findings: readonly PreCommitBlockingFinding[];
} {
  const actorId = normalizeOptionalText(process.env.ATM_COMMIT_ACTOR_ID);
  const taskId = normalizeOptionalText(process.env.ATM_COMMIT_TASK_ID);
  const claimLeaseId = normalizeOptionalText(process.env.ATM_COMMIT_CLAIM_LEASE_ID);
  const sessionId = normalizeOptionalText(process.env.ATM_COMMIT_SESSION_ID);
  const stagedTaskIds = inferTaskIdsFromStagedFiles(stagedFiles);
  const pendingBatchCheckpointTaskId = resolvePendingBatchCheckpointTaskId(cwd, stagedFiles);
  const findings: PreCommitBlockingFinding[] = [];
  const suggestedTaskId = pendingBatchCheckpointTaskId ?? (stagedTaskIds.length === 1 ? stagedTaskIds[0] : stagedTaskIds[0] ?? null);
  const actorRegistryState = inspectTrackedActorRegistryState(cwd);

  if (actorRegistryState.blocking) {
    const governedRecoveryCommand = suggestedTaskId
      ? `git add ${quoteCliValue(actorRegistryState.path)} && node atm.mjs git commit --actor <id> --task ${suggestedTaskId} --message "<summary>" --json`
      : `node atm.mjs git commit --actor <id> --message "<summary>" --json`;
    findings.push({
      code: 'ATM_COMMIT_ACTOR_REGISTRY_UNSTAGED',
      source: 'commit-attribution',
      detail: `Tracked actor registry ${actorRegistryState.path} has ${actorRegistryState.status === 'mixed' ? 'both staged and unstaged' : 'unstaged'} changes. Governed node atm.mjs git commit can auto-stage that tracked registry when it belongs to the governed commit surface, but bare git commit cannot. Re-run through the ATM wrapper or restore the drift first.`,
      requiredCommand: governedRecoveryCommand,
      classification: 'current-task'
    });
    return {
      ok: false,
      findings
    };
  }

  if (!actorId && !taskId && !sessionId) {
    if (stagedTaskIds.length > 0) {
      const wrapperRequired: PreCommitBlockingFinding = {
        code: 'ATM_GIT_COMMIT_WRAPPER_REQUIRED',
        source: 'commit-attribution',
        detail: 'Staged ATM task/evidence changes must commit through node atm.mjs git commit so ATM can bind author, session, claim, and trailers consistently. Direct git commit remains valid for read-only git and non-governed maintenance.',
        requiredCommand: suggestedTaskId
          ? `node atm.mjs git commit --actor <id> --task ${suggestedTaskId} --message "<summary>" --json`
          : 'node atm.mjs git commit --actor <id> --task <task> --message "<summary>" --json',
        classification: 'current-task'
      };
      return {
        ok: false,
        findings: [wrapperRequired]
      };
    }
    return {
      ok: true,
      findings
    };
  }

  const effectiveTaskId = taskId ?? pendingBatchCheckpointTaskId ?? (stagedTaskIds.length === 1 ? stagedTaskIds[0] : null);
  if (!actorId || (stagedTaskIds.length > 0 && !effectiveTaskId)) {
    findings.push({
      code: 'ATM_GIT_COMMIT_WRAPPER_REQUIRED',
      source: 'commit-attribution',
      detail: 'Governed task commits must use node atm.mjs git commit --actor <id> --task <task> --message "<summary>" so ATM can bind author, session, claim, and trailers consistently. Bare git commit is not banned for inspection or non-governed files.',
      requiredCommand: 'node atm.mjs git commit --actor <id> --task <task> --message "<summary>" --json',
      classification: 'current-task'
    });
    return {
      ok: false,
      findings
    };
  }

  if (!effectiveTaskId) {
    const expectedIdentity = resolveExpectedGitIdentityForActor(cwd, actorId);
    findings.push(...collectIdentityProvenanceFindings(actorId, expectedIdentity));
    const authorName = normalizeOptionalText(process.env.GIT_AUTHOR_NAME) ?? runGitScalar(cwd, ['config', '--local', '--get', 'user.name']);
    const authorEmail = normalizeOptionalText(process.env.GIT_AUTHOR_EMAIL) ?? runGitScalar(cwd, ['config', '--local', '--get', 'user.email']);
    if (!expectedIdentity.gitName || !expectedIdentity.gitEmail) {
      findings.push({
        code: 'ATM_COMMIT_IDENTITY_PROFILE_MISSING',
        source: 'commit-attribution',
        detail: `Actor ${actorId} has no resolved git identity profile in actor registry or .atm/runtime/identity/default.json.`,
        requiredCommand: buildIdentitySetRequiredCommand(cwd, actorId),
        classification: 'current-task'
      });
    } else {
      if (authorName !== expectedIdentity.gitName) {
        findings.push({
          code: 'ATM_COMMIT_AUTHOR_NAME_MISMATCH',
          source: 'commit-attribution',
          detail: `Commit author name is ${authorName ?? 'unset'}, expected ${expectedIdentity.gitName}.`,
          classification: 'current-task'
        });
      }
      if (authorEmail !== expectedIdentity.gitEmail) {
        findings.push({
          code: 'ATM_COMMIT_AUTHOR_EMAIL_MISMATCH',
          source: 'commit-attribution',
          detail: `Commit author email is ${authorEmail ?? 'unset'}, expected ${expectedIdentity.gitEmail}.`,
          classification: 'current-task'
        });
      }
    }
    return {
      ok: findings.length === 0,
      findings
    };
  }

  const task = readJsonFile(path.join(cwd, '.atm', 'history', 'tasks', `${effectiveTaskId}.json`));
  const claim = parseHookTaskClaim(task?.claim);
  const mirrorSyncOnly = inspectMirrorSyncOnlyStagedArtifacts(cwd, effectiveTaskId, stagedFiles);
  const historicalLedgerRestore = inspectHistoricalLedgerRestoreStagedArtifacts(cwd, effectiveTaskId, stagedFiles);
  const closeCommitWindow = inspectCloseCommitWindowStagedArtifacts(cwd, effectiveTaskId, stagedFiles);
  const pendingBatchCheckpoint = inspectPendingBatchCheckpointStagedArtifacts(cwd, effectiveTaskId, stagedFiles);
  const bypassesActiveSession = mirrorSyncOnly.ok || historicalLedgerRestore.ok || closeCommitWindow.ok || pendingBatchCheckpoint.ok;
  const claimForSession = bypassesActiveSession ? null : claim;
  const session = bypassesActiveSession && !sessionId ? null : resolveActorWorkSession(cwd, {
    sessionId,
    actorId,
    taskId: effectiveTaskId,
    claimLeaseId: claimLeaseId ?? claimForSession?.leaseId ?? null,
    includeNonActive: true
  });
  if (!session && !bypassesActiveSession) {
    findings.push({
      code: 'ATM_COMMIT_SESSION_MISSING',
      source: 'commit-attribution',
      detail: `No ATM work session matched actor ${actorId} and task ${effectiveTaskId}. Claim the task through ATM before committing.`,
      requiredCommand: `node atm.mjs next --claim --actor ${actorId} --prompt "${effectiveTaskId}" --json`,
      classification: 'current-task'
    });
    return {
      ok: false,
      findings
    };
  }
  if (session && session.actorId !== actorId) {
    findings.push({
      code: 'ATM_COMMIT_SESSION_ACTOR_MISMATCH',
      source: 'commit-attribution',
      detail: `Session ${session.sessionId} belongs to ${session.actorId}, not ${actorId}.`,
      classification: 'current-task'
    });
  }
  if (session && session.taskId !== effectiveTaskId) {
    findings.push({
      code: 'ATM_COMMIT_SESSION_TASK_MISMATCH',
      source: 'commit-attribution',
      detail: `Session ${session.sessionId} is for ${session.taskId}, not ${effectiveTaskId}.`,
      classification: 'current-task'
    });
  }
  if (session && (claimLeaseId ?? claimForSession?.leaseId ?? null) && session.claimLeaseId !== (claimLeaseId ?? claimForSession?.leaseId ?? null)) {
    findings.push({
      code: 'ATM_COMMIT_SESSION_CLAIM_MISMATCH',
      source: 'commit-attribution',
      detail: `Session ${session.sessionId} is bound to claim ${session.claimLeaseId ?? 'unset'}, not ${(claimLeaseId ?? claimForSession?.leaseId) ?? 'unset'}.`,
      classification: 'current-task'
    });
  }
  const expectedIdentity = resolveExpectedGitIdentityForActor(cwd, actorId);
  findings.push(...collectIdentityProvenanceFindings(actorId, expectedIdentity));
  const authorName = normalizeOptionalText(process.env.GIT_AUTHOR_NAME) ?? runGitScalar(cwd, ['config', '--local', '--get', 'user.name']);
  const authorEmail = normalizeOptionalText(process.env.GIT_AUTHOR_EMAIL) ?? runGitScalar(cwd, ['config', '--local', '--get', 'user.email']);
  if (!expectedIdentity.gitName || !expectedIdentity.gitEmail) {
    findings.push({
      code: 'ATM_COMMIT_IDENTITY_PROFILE_MISSING',
      source: 'commit-attribution',
      detail: `Actor ${actorId} has no resolved git identity profile in actor registry or .atm/runtime/identity/default.json.`,
      requiredCommand: buildIdentitySetRequiredCommand(cwd, actorId),
      classification: 'current-task'
    });
  } else {
    if (authorName !== expectedIdentity.gitName) {
      findings.push({
        code: 'ATM_COMMIT_AUTHOR_NAME_MISMATCH',
        source: 'commit-attribution',
        detail: `Commit author name is ${authorName ?? 'unset'}, expected ${expectedIdentity.gitName}.`,
        classification: 'current-task'
      });
    }
    if (authorEmail !== expectedIdentity.gitEmail) {
      findings.push({
        code: 'ATM_COMMIT_AUTHOR_EMAIL_MISMATCH',
        source: 'commit-attribution',
        detail: `Commit author email is ${authorEmail ?? 'unset'}, expected ${expectedIdentity.gitEmail}.`,
        classification: 'current-task'
      });
    }
  }

  return {
    ok: findings.length === 0,
    findings
  };
}

